#include<iostream>
using namespace std;

int main()
{

    int lowerLimit, upperLimit , i;

    cout<<"Enter Lower Limit "<<endl;
    cin>>lowerLimit;

    cout<<"Enter Upper Limit "<<endl;
    cin>>upperLimit;

    for(i=lowerLimit; i<=upperLimit; i++){
        if(i==0){
        cout<< i <<endl;
        }

    else{
        cout<< i <<endl;
    }
    }
    return 0;
}
